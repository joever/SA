    private void Carrega(JComboBox DadosTipo, String[][] sTipo) {
        if (sTipo != null){
            if (sTipo.length > 0){
                for (int i=0;i<sTipo.length;i++){
                    DadosTipo.addItem(sTipo[i][0]+" - "+sTipo[i][1]+" - "+sTipo[i][2]);
                }
            }
        }
    }
    
    public void atualiza() {
        Integer cont, minimo, maximo;
        Double valor, tempo;
        
        //Verificações
        if (vTempo.getText().length() > 0)
            tempo = Double.parseDouble(vTempo.getText())*1000;
        else
            tempo = 10000.0; //Padrão 10s
        
        if (vMinimo.getText().length() > 0)
            minimo = Integer.parseInt(vMinimo.getText());
        else
            minimo = 20; //Padrão valores entre 20 - 40
        
        if (vMaximo.getText().length() > 0)
            maximo = Integer.parseInt(vMaximo.getText()) - minimo;
        else
            maximo = 0; //Padrão valores entre 20 - 40
        //Início do loop
        try {
            while (true){
                Date data = new Date();
                valor = minimo + (maximo*Math.random());
                valor = Math.rint(valor*100)/100;
                if (!BancoUtil.iEntrada(Integer.parseInt(sTipo[DadosTipo.getSelectedIndex()][0]),valor.toString(),data)) {
                    JOptionPane.showMessageDialog(rootPane, "Não foi possível incluir o registro!", "Erro", ERROR);
                }
                else {
                    cont = Integer.parseInt(vQuant.getText()) + 1;
                    vQuant.setText(cont.toString());
                }
                if (parar) {
                    vAndamento.setIndeterminate(false);
                    return;
                }
                Thread.sleep(tempo.longValue());
            }
        }catch (SQLException ex) {
            Logger.getLogger(RSSF.class.getName()).log(Level.SEVERE, null, ex);
        } catch (InterruptedException ex) {
            return;
        }
    }
