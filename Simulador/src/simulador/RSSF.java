    private void bSimularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSimularActionPerformed
        // TODO add your handling code here:
        bSimular.setEnabled(false);
        bFechar.setEnabled(false);
        bParar.setEnabled(true);
        if (t1.getState() == Thread.State.NEW) {
            vAndamento.setIndeterminate(true);
            t1.start();
        }
        else {
            parar = false;
            if (t1.getState() == Thread.State.TERMINATED) {
                this.t1 = new Thread() {
                    public void run() {
                        atualiza();
                    }
                };
                vAndamento.setIndeterminate(true);
                t1.start();
            }
        }
    }//GEN-LAST:event_bSimularActionPerformed

    private void bPararActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bPararActionPerformed
        // TODO add your handling code here:
        parar = true;
        bParar.setEnabled(false);
        bFechar.setEnabled(true);
        bSimular.setEnabled(true);
    }//GEN-LAST:event_bPararActionPerformed

