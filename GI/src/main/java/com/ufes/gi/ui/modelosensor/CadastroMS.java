    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (txtNome.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Favor informar o Nome para o Modelo do Sensor.", "Aviso", WIDTH);
            txtNome.grabFocus();
        }
        else if (txtDescricao.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Favor informar a Descrição do Modelo do Sensor.", "Aviso", WIDTH);
            txtDescricao.grabFocus();
        }
        else if (txtNSensores.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Favor informar o número de sensores que o nó sensor poderá ter.", "Aviso", WIDTH);
            txtDescricao.grabFocus();
        }
        else {
            ModeloSensor newMS = new ModeloSensor();
            newMS.setIdModeloSensor(Integer.parseInt(txtCodigo.getText()));
            newMS.setNome(txtNome.getText());
            newMS.setDescricao(txtDescricao.getText());
            newMS.setnSensores(Integer.parseInt(txtNSensores.getText()));
            cadastraMS(newMS);
        }
    }//GEN-LAST:event_jButton1ActionPerformed


