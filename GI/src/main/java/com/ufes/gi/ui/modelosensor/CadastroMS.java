    private void cadastraMS(ModeloSensor myMS){
        ModeloSensorDAO myDAO = new ModeloSensorDAO();
        if (myDAO.adiciona(myMS)){
            JOptionPane.showMessageDialog(rootPane, "Modelo de Sensor cadastrado com sucesso!", "Informação", JOptionPane.INFORMATION_MESSAGE);
            txtCodigo.setText(myDAO.getProxId().toString());
            txtNome.setText("");
            txtDescricao.setText("");
            txtNSensores.setText("0");
            txtNome.grabFocus();
        }
        else
            JOptionPane.showMessageDialog(rootPane, "Falha ao cadastrado modelo de sensor!", "Erro", JOptionPane.ERROR_MESSAGE);
    }
    
